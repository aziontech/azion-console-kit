          @update:modelValue="selectTheme"
          optionValue="value"
          optionLabel="name"
          :loading="!selectedTheme?.value"
          :options="themeOptions"
          :autoOptionFocus="false"
          :pt="{
            root: {
              class: ' w-auto py-0 h-[30px] items-center align-middle',
