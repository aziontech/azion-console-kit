<script>
  import ListTableBlock from '@/templates/list-table-block'
  import EmptyResultsBlock from '@/templates/empty-results-block'
  import Illustration from '@/assets/svg/illustration-layers.vue'

  export default {
    name: 'data-streaming-view',
    components: {
      ListTableBlock,
      EmptyResultsBlock,
      Illustration
    },
    props: {
      listDataStreamingService: {
        required: true,
        type: Function
      },
      deleteDataStreamingService: {
        required: true,
        type: Function
      },
      documentationService: {
        required: true,
        type: Function
      }
    },
    data: () => ({
      hasContentToList: true
    }),
    computed: {
      getColumns() {
        return [
          {
            field: 'name',
            header: 'Name'
          },
          {
            field: 'dataSource',
            header: 'Data Source'
          },
          {
            field: 'templateName',
            header: 'Template'
          },
          {
            field: 'active',
            header: 'Active'
          }
        ]
      }
    },
    methods: {
      handleLoadData(event) {
        this.hasContentToList = event
      }
    }
  }
</script>

<template>
  <ListTableBlock
    v-if="hasContentToList"
    pageTitle="Data Streaming"
    pageTitleDelete="Data Streaming"
    addButtonLabel="Data Streaming"
    createPagePath="/data-streaming/create"
    editPagePath="/data-streaming/edit"
    :listService="listDataStreamingService"
    :deleteService="deleteDataStreamingService"
    :columns="getColumns"
    @on-load-data="handleLoadData"
  ></ListTableBlock>
  <EmptyResultsBlock
    v-else
    pageTitle="Data Streaming"
    title="No data streaming added"
    description="Create your first data streaming."
    createButtonLabel="Data Streaming"
    createPagePath="data-streaming/create"
    :documentationService="documentationService"
  >
    <template #illustration>
      <Illustration />
    </template>
  </EmptyResultsBlock>
</template>
